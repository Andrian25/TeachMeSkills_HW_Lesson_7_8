package com.teahmeskills.lesson_7_8.Task_1;

public abstract class AllVector {

    protected double x, y;

    public AllVector(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

}